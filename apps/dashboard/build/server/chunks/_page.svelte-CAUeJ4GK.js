class JobPoller {
  constructor(options) {
    this.options = options;
    this.minIntervalMs = options.minIntervalMs ?? 500;
    this.maxIntervalMs = options.maxIntervalMs ?? 3e4;
  }
  timer = null;
  inFlight = false;
  stopped = false;
  minIntervalMs;
  maxIntervalMs;
  start() {
    if (this.stopped) return;
    void this.tick();
  }
  /** Trigger an immediate poll on user interaction (e.g. tab focus). */
  poke() {
    if (this.stopped) return;
    if (this.timer) {
      clearTimeout(this.timer);
      this.timer = null;
    }
    void this.tick();
  }
  stop() {
    this.stopped = true;
    if (this.timer) {
      clearTimeout(this.timer);
      this.timer = null;
    }
  }
  clampInterval(suggestedMs) {
    return Math.min(Math.max(suggestedMs, this.minIntervalMs), this.maxIntervalMs);
  }
  async tick() {
    if (this.stopped || this.inFlight) return;
    this.inFlight = true;
    let envelope = null;
    try {
      envelope = await this.options.fetch();
      if (envelope) this.options.onUpdate?.(envelope);
    } catch (error) {
      const handler = this.options.onError ?? ((err) => console.error("JobPoller error:", err));
      handler(error);
    } finally {
      this.inFlight = false;
    }
    if (this.stopped) return;
    if (envelope && (envelope.job.status === "completed" || envelope.job.status === "failed" || envelope.job.status === "canceled")) {
      return;
    }
    const delay = this.clampInterval(envelope?.nextPollMs ?? 2e3);
    this.timer = setTimeout(() => void this.tick(), delay);
  }
}
